# Operator precedence adalah operasi mana yang didahulukan yaitu
# 1. Operasi di dalam tanda kurung
# 2. Operasi Perpangkatan dan kawan-kawan seperti modulus
# 3. Operasi Perkalian dan Pembagian dan kawan-kawan seperti floor division
# 4. Operasi Pertambahan dan Pengurangan

operasi = 12 // 5 * 2 
print(operasi)

operasi = (4 - 2) * 12 // 2 ** 2
print(operasi)

operasi = 12 % 2 ** 2
print(operasi)