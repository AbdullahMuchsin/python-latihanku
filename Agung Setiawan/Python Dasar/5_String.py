name = "Abdullah 'Muchsin'"
name2 = 'Abdullah "Muchsin"'
print(name)
print(name2)

print(name[0]) # Mengambil karakter dari string dari index awal yaitul 0
print(name[-1]) # Mengambil karakter dari paling akhir string dengan -1 
print(name[-2]) # Mengambil karakter dari akhir ke-2 dengan -2

print(name2[0:8]) # Mengambil karakter dengan range(sampai index berapa dengan tanda : )
# Diatas artinya ambil karakter dari index 0 sampai kurang dari index 8
print(name2[9:]) # artinya mengambil karakter dari index 9 sampai akhir
print(name2[:8]) # artinya mengambil karakter dari awal sampai kurang dari index 8

print(name2[:]) # artinya mengambil dari awal index sampai dengan akhir index