# LATIHAN IF DAN ELSE STATEMENT

nama = input("Masukan nama anda : ")

# 1. Menggunakan if inline
if nama == "Marsudin": print("Hai",nama,"kamu keren...")
print("program berakhir\n".upper())

# 2. Menggunkan if indentation
if nama == "Surudin":
    print("Kamu keren " + nama)
    print("Dan jago masak kamu iya")
print("program berakhir\n".upper())

# 3. Menggunakan else statement indentation
if nama == "Surudin":
    print("Wow, nama kamu keren banget...")
else:
    print("Nama kamu bagus")
print("program berakhir\n".upper())

