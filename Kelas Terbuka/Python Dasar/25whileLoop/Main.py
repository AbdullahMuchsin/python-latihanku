# LATIHAN OPERASI WHILE LOOP

print("PERULANGAN".center(20,"=") + "\n")

inp = int(input("Masukan awal angka perulangan : "))
print(f"Dimulai dari angka yang anda masukan + 1 : ")
while inp < 10:
    inp += 1
    print(f"Angka sekarang adalah : {inp}")
print("AKHIR PROGRAM".center(20,"=") + "\n")

